# Create a list containing a number of names.
# Display the elements of the list, one on each line
my_list = ["John", "Kim", "Jerry", "Liz", "Mary"]
print("The elements of the list", my_list, "are")
for name in my_list:
    print(name)
print()

# Display the characters of an input string
input_string = input("Enter a string of characters: ")
print(f'The characters of {input_string} are:')
for char in input_string:
    print(char)
print()
